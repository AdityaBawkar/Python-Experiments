# Experiment 3: List Operations
# Objective: To demonstrate basic list operations in Python

# Creating a list
numbers = [10, 5, 8, 3]

print("Original List:", numbers)

# Adding an element
numbers.append(15)
print("After appending 15:", numbers)

# Removing an element
numbers.remove(8)
print("After removing 8:", numbers)

# Sorting the list
numbers.sort()
print("After sorting:", numbers)

# Reversing the list
numbers.reverse()
print("After reversing:", numbers)
